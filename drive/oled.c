/**********************************************
//OLED Start
**********************************************/
void OLED_Start()
{
	OLED_SCL_high();
	nrf_delay_us(4);
   OLED_SDA_high();
	nrf_delay_us(4);
   OLED_SDA_low();
	nrf_delay_us(4);
   OLED_SCL_low();
	nrf_delay_us(4);
}

/**********************************************
//OLED Stop
**********************************************/
void OLED_Stop()
{
   OLED_SCL_low();
	nrf_delay_us(4);
   OLED_SDA_low();
	nrf_delay_us(4);
   OLED_SCL_high();
	nrf_delay_us(4);
   OLED_SDA_high();
	nrf_delay_us(4);
}
/**********************************************
// OLED Write byte
**********************************************/
void Write_OLED_Byte(unsigned char OLED_Byte)
{
	unsigned char i;
	for(i=0; i<8; i++)		
	{
		if(OLED_Byte & 0x80)
			OLED_SDA_high();
		else
			OLED_SDA_low();
		nrf_delay_us(4);
		OLED_SCL_high();
		nrf_delay_us(4);
		OLED_SCL_low();
		nrf_delay_us(4);
		OLED_Byte <<= 1;
	}
	OLED_SDA_high();
	nrf_delay_us(4);
	OLED_SCL_high();
	nrf_delay_us(4);
	OLED_SCL_low();
}
/**********************************************
// OLED Write Command
**********************************************/
void Write_OLED_Command(unsigned char OLED_Command)
{
   OLED_Start();
   Write_OLED_Byte(0x78);            //Slave address,SA0=0
   Write_OLED_Byte(0x00);			//write command
   Write_OLED_Byte(OLED_Command); 
   OLED_Stop();
}
/**********************************************
// OLED Write Data
**********************************************/
void Write_OLED_Data(unsigned char OLED_Data)
{
   OLED_Start();
   Write_OLED_Byte(0x78);			//D/C#=0; R/W#=0
   Write_OLED_Byte(0x40);			//write data
   Write_OLED_Byte(OLED_Data);
   OLED_Stop();
}

